#!/usr/bin/env node
/**
 * T2 — Generate theme-contract/literal/contract.css.ts
 *
 * Reads the census (all 27 themes) and emits a single shared VE contract file
 * containing every CSS class token encountered in any theme's bootstrap.css.
 *
 * Each export carries a `// .class-name` provenance comment so the registry
 * reader can reverse-map class→symbol. Symbols use strict 1:1 kebab→camelCase;
 * collisions with element contract names are resolved with a `cls` prefix.
 *
 * Usage:
 *   node scripts/generate-ve-literal/generate-contract.mjs [--dry-run]
 */

import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'

import { VE2_CONTRACT_ROOT } from '../generate-ve-theme/constants.mjs'
import { ALL_THEMES, buildCensus } from './registry.mjs'

const OUT_FILE = path.join(VE2_CONTRACT_ROOT, 'literal', 'contract.css.ts')

/**
 * Bootstrap component prefix → section label (best-effort, alphabetical fallback).
 * Governs the // ---- Section ---- grouping headers in the output.
 */
const SECTION_PREFIXES = [
	['accordion', 'Accordion'],
	['alert', 'Alerts'],
	['badge', 'Badge'],
	['breadcrumb', 'Breadcrumb'],
	['btn', 'Buttons'],
	['card', 'Card'],
	['carousel', 'Carousel'],
	['col', 'Grid'],
	['collapse', 'Collapse'],
	['container', 'Grid'],
	['dropdown', 'Dropdowns'],
	['figure', 'Figures'],
	['form', 'Forms'],
	['gap', 'Flex/Grid utilities'],
	['img', 'Images'],
	['input', 'Forms'],
	['list', 'List Group'],
	['modal', 'Modal'],
	['nav', 'Navigation'],
	['navbar', 'Navbar'],
	['offcanvas', 'Offcanvas'],
	['page', 'Pagination'],
	['pagination', 'Pagination'],
	['placeholder', 'Placeholders'],
	['popover', 'Popovers'],
	['progress', 'Progress'],
	['ratio', 'Ratio'],
	['row', 'Grid'],
	['spinner', 'Spinners'],
	['tab', 'Navigation'],
	['table', 'Tables'],
	['toast', 'Toasts'],
	['tooltip', 'Tooltips'],
]

function sectionFor(cssClass) {
	const lower = cssClass.toLowerCase()
	for (const [prefix, section] of SECTION_PREFIXES) {
		if (lower === prefix || lower.startsWith(`${prefix}-`)) return section
	}
	return 'Utilities / Misc'
}

export async function generateContractFile(opts = {}) {
	const { dryRun = false, themes = ALL_THEMES } = opts

	console.log('Building census …')
	const census = await buildCensus(themes)
	const { classMap, collisions } = census

	// Group classes by section for readability
	const sections = new Map()
	for (const [cssClass, symbol] of classMap) {
		const section = sectionFor(cssClass)
		if (!sections.has(section)) sections.set(section, [])
		sections.get(section).push({ cssClass, symbol })
	}

	// Sort entries within each section by cssClass
	for (const entries of sections.values()) {
		entries.sort((a, b) => a.cssClass.localeCompare(b.cssClass))
	}

	// Sort sections alphabetically
	const sortedSections = [...sections.entries()].sort(([a], [b]) => a.localeCompare(b))

	const lines = [
		"import { style } from '@vanilla-extract/css'",
		'',
		'/**',
		' * Shared contract file for the v2 literal CSS→VE converter.',
		' * Generated by scripts/generate-ve-literal/generate-contract.mjs — do not hand-edit.',
		' *',
		` * Covers ${classMap.size} CSS class tokens across all 27 Bootstrap + Bootswatch themes.`,
		' * Each export carries a `// .class-name` provenance comment for registry round-tripping.',
		' * Collision-renamed contracts (e.g. `.h1`→`clsH1`) are documented inline.',
		' */',
		'',
	]

	// Note collisions at top
	if (collisions.size > 0) {
		lines.push(
			'// Collision renames: class symbol matched an element contract name → cls prefix applied.',
		)
		for (const [origSymbol, cssClass] of [...collisions].sort()) {
			const renamedSymbol = `cls${origSymbol.charAt(0).toUpperCase()}${origSymbol.slice(1)}`
			lines.push(`//   .${cssClass} → "${origSymbol}" → renamed "${renamedSymbol}"`)
		}
		lines.push('')
	}

	for (const [section, entries] of sortedSections) {
		lines.push(`// ${'─'.repeat(60)}`)
		lines.push(`// ${section}`)
		lines.push(`// ${'─'.repeat(60)}`)
		lines.push('')
		for (const { cssClass, symbol } of entries) {
			lines.push(`// .${cssClass}`)
			lines.push(`export const ${symbol} = style({})`)
		}
		lines.push('')
	}

	const content = lines.join('\n')

	if (dryRun) {
		console.log(`[dry-run] Would write ${lines.length} lines to ${OUT_FILE}`)
		console.log(`  Sections: ${sortedSections.length}, Classes: ${classMap.size}`)
		return { content, outFile: OUT_FILE, classCount: classMap.size }
	}

	await mkdir(path.dirname(OUT_FILE), { recursive: true })
	await writeFile(OUT_FILE, content, 'utf8')
	console.log(`Wrote ${classMap.size} class contracts to ${OUT_FILE}`)

	return { content, outFile: OUT_FILE, classCount: classMap.size }
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

async function main() {
	const args = process.argv.slice(2)
	const dryRun = args.includes('--dry-run')

	await generateContractFile({ dryRun })
}

if (process.argv[1].endsWith('generate-contract.mjs')) {
	main().catch((err) => {
		console.error(err)
		process.exit(1)
	})
}
