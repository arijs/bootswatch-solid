import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

import { VE2_CONTRACT_ROOT } from './constants.mjs'
import { resetContractRegistryCache } from '../generate-ve-theme/contract-registry.mjs'

const GENERATED_DIR = path.join(VE2_CONTRACT_ROOT, 'utilities', 'generated')
const GENERATED_CONTRACT = path.join(GENERATED_DIR, 'contract.css.ts')
const GENERATED_INDEX = path.join(GENERATED_DIR, 'index.css.ts')

/** `.col-md-6` → `colMd6` */
export function classSelectorToContractSymbol(classSelector) {
	const name = classSelector.replace(/^\./, '')
	const parts = name.split('-')
	if (parts.length === 0) return null
	const [first, ...rest] = parts
	const camel =
		first +
		rest
			.map((part) => {
				if (/^\d/.test(part)) return part
				return part.charAt(0).toUpperCase() + part.slice(1)
			})
			.join('')
	const symbol = camel.replace(/[^a-zA-Z0-9_$]/g, '')
	if (!symbol || !/^[a-zA-Z]/.test(symbol)) return null
	return symbol
}

export function extractClassTokensFromSelector(selector) {
	const tokens = []
	const re = /\.([a-z0-9_-]+)/gi
	let match
	while ((match = re.exec(selector)) !== null) {
		tokens.push(`.${match[1]}`)
	}
	return tokens
}

/**
 * Collect unmapped class selectors from skip report entries.
 */
export function collectUnmappedClasses(skippedEntries, registry) {
	const classes = new Set()
	for (const { selector, warning } of skippedEntries) {
		if (!warning.includes('UNMAPPED_SELECTOR') || !warning.includes('class')) continue
		for (const cls of extractClassTokensFromSelector(selector)) {
			if (registry.resolveContractForSelector(cls)) continue
			classes.add(cls)
		}
	}
	return [...classes].sort()
}

async function readExistingGeneratedEntries() {
	const entries = new Map()
	try {
		const source = await readFile(GENERATED_CONTRACT, 'utf8')
		const lines = source.split('\n')
		for (let i = 0; i < lines.length; i++) {
			const comment = lines[i].match(/^\/\/\s*(\.[a-z0-9][\w-]*)\s*$/i)
			if (!comment) continue
			const next = lines[i + 1] ?? ''
			const sym = next.match(/export const (\w+) = style\(\{\}\)/)
			if (sym) entries.set(comment[1], sym[1])
		}
	} catch {
		// no file yet
	}
	return entries
}

/**
 * Append new contract exports for unmapped Bootstrap classes.
 */
export async function ensureAutoContracts(classSelectors) {
	if (classSelectors.length === 0) return { added: 0 }

	await mkdir(GENERATED_DIR, { recursive: true })
	const entries = await readExistingGeneratedEntries()
	const usedSymbols = new Set(entries.values())
	const newEntries = []

	for (const cls of classSelectors) {
		if (entries.has(cls)) continue
		let symbol = classSelectorToContractSymbol(cls)
		if (!symbol) continue
		let deduped = symbol
		let n = 2
		while (usedSymbols.has(deduped)) {
			deduped = `${symbol}${n}`
			n++
		}
		symbol = deduped
		usedSymbols.add(symbol)
		entries.set(cls, symbol)
		newEntries.push({ cls, symbol })
	}

	if (newEntries.length === 0) return { added: 0 }

	const lines = [
		"import { style } from '@vanilla-extract/css'",
		'',
		'// AUTO-GENERATED by generate-ve-theme-literal — unmapped Bootstrap utility/component classes.',
		'',
	]

	const sorted = [...entries.entries()].sort((a, b) => a[0].localeCompare(b[0]))
	for (const [cls, symbol] of sorted) {
		lines.push(`// ${cls}`)
		lines.push(`export const ${symbol} = style({})`)
		lines.push('')
	}

	await writeFile(GENERATED_CONTRACT, `${lines.join('\n')}\n`, 'utf8')
	await writeFile(GENERATED_INDEX, "export * from './contract.css'\n", 'utf8')

	resetContractRegistryCache()
	return { added: newEntries.length }
}
