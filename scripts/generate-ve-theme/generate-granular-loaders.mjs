#!/usr/bin/env node
/**
 * G3 — Regenerate the granular loader artifacts from the canonical family set.
 *
 * Emits, deterministically from EMIT_FAMILIES + the 27-theme list:
 *   1. themes/{theme}/theme.ts        — full-theme aggregator (global + every chunk)
 *   2. theme-runtime/theme-runtime.ts — scopes, theme keys, full + per-family loader maps
 *
 * The loadable family set is `['global', ...EMIT_FAMILIES]` (the same set
 * validate-family-chunks proves equals the monolith). `global` carries reboot,
 * root vars and keyframes, so it loads first in the aggregator and is wired into
 * the per-family map (Ve2GranularShell loads it as the always-requested baseline).
 *
 *   node scripts/generate-ve-theme/generate-granular-loaders.mjs
 */
import { writeFile } from 'node:fs/promises'
import path from 'node:path'
import { ALL_THEMES } from '../generate-ve-literal/registry.mjs'
import { EMIT_FAMILIES, themeScopeExportName, VE2_ROOT, VE2_THEMES_ROOT } from './constants.mjs'

// Loadable families: global first (reboot/vars/keyframes), then every emitted chunk.
const LOADABLE_FAMILIES = ['global', ...EMIT_FAMILIES]

const familyKey = (f) => (f.includes('/') ? `'${f}'` : f)

function aggregatorSource() {
	return `${LOADABLE_FAMILIES.map((f) => `import './${f}/styles.css'`).join('\n')}\n`
}

function runtimeSource() {
	const keys = ALL_THEMES.map((t) => `\t'${t}',`).join('\n')
	// Scopes load dynamically: each scope.css carries the active theme's body/
	// typography/canvas styles as a side effect. A static map would import all 27
	// scope.css side-effect modules for a single-key lookup (see Ve2GranularShell).
	const scopeLoaders = ALL_THEMES.map(
		(t) =>
			`\t${t}: () => import('../themes/${t}/scope.css').then((m) => m.${themeScopeExportName(t)}),`,
	).join('\n')
	const themeLoaders = ALL_THEMES.map(
		(t) => `\t${t}: () => import('../themes/${t}/theme.js'),`,
	).join('\n')

	const familyLoaders = ALL_THEMES.map((t) => {
		const entries = LOADABLE_FAMILIES.map(
			(f) => `\t\t${familyKey(f)}: () => import('../themes/${t}/${f}/styles.css'),`,
		).join('\n')
		return `\t${t}: {\n${entries}\n\t},`
	}).join('\n')

	const resolveCases = ALL_THEMES.filter((t) => t !== 'bootstrap')
		.map((t) => `\t\tcase '${t}':`)
		.join('\n')

	return `import type { Ve2StyleFamily } from './style-families'

// Generated by scripts/generate-ve-theme/generate-granular-loaders.mjs — do not edit by hand.
export const VE2_THEME_KEYS = [
${keys}
] as const

export type Ve2ThemeKey = (typeof VE2_THEME_KEYS)[number]

export type Ve2StyleFamilyLoaderMap = Partial<Record<Ve2StyleFamily, () => Promise<unknown>>>

const ve2ThemeScopeLoaders: Record<Ve2ThemeKey, () => Promise<string>> = {
${scopeLoaders}
}

export const ve2ThemeLoaders: Record<Ve2ThemeKey, () => Promise<unknown>> = {
${themeLoaders}
}

export const ve2ThemeFamilyLoaders: Record<Ve2ThemeKey, Ve2StyleFamilyLoaderMap> = {
${familyLoaders}
}

export function resolveVe2ThemeKey(rawTheme: string | null | undefined): Ve2ThemeKey {
	switch (rawTheme) {
${resolveCases}
			return rawTheme
		default:
			return 'bootstrap'
	}
}

// Dynamically loads the active theme's scope.css (side-effect styles + class name).
// Only the requested theme's scope module is fetched — see Ve2GranularShell.
export function loadVe2ThemeScope(rawTheme: string | null | undefined): Promise<string> {
	return ve2ThemeScopeLoaders[resolveVe2ThemeKey(rawTheme)]()
}
`
}

async function main() {
	for (const theme of ALL_THEMES) {
		await writeFile(path.join(VE2_THEMES_ROOT, theme, 'theme.ts'), aggregatorSource())
	}
	await writeFile(
		path.join(VE2_ROOT, 'src', 'theme-runtime', 'theme-runtime.ts'),
		runtimeSource(),
	)
	console.log(
		`[loaders] wrote ${ALL_THEMES.length} theme.ts aggregators + theme-runtime.ts ` +
			`(${LOADABLE_FAMILIES.length} loadable families each)`,
	)
}

main().catch((e) => {
	console.error(e)
	process.exit(1)
})
